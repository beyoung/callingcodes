// 辅助函数
export const getCountryInfoByCode = (code: string): CountryInfo | undefined => {
  return countryInfoDict.find(
    (country) =>
      country.code === code || country.iso2 === code || country.iso3 === code
  );
};

export const getCountryInfoByCallingCode = (
  callingCode: string
): CountryInfo[] => {
  return countryInfoDict.filter(
    (country) => country.callingCode === callingCode
  );
};

export const searchCountryInfo = (query: string): CountryInfo[] => {
  const lowerQuery = query.toLowerCase();
  return countryInfoDict.filter(
    (country) =>
      country.name.toLowerCase().includes(lowerQuery) ||
      country.nameZh.includes(query) ||
      country.code.toLowerCase().includes(lowerQuery) ||
      country.capital.toLowerCase().includes(lowerQuery)
  );
};
